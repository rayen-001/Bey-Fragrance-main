async function main() {
  console.log('🚀 Starting category repair...');
  let updateCount = 0;
  let missingCount = 0;

  for (const [name, category] of Object.entries(mapping)) {
    try {
      const result = await prisma.product.updateMany({
        where: {
          name: name,
          brand: "Hama Fragrance"
        },
        data: {
          category: category
        }
      });

      if (result.count > 0) {
        updateCount += result.count;
      } else {
        missingCount++;
        // console.warn(`⚠️ No product found for: ${name}`);
      }
    } catch (e) {
      console.error(`❌ Failed to update ${name}:`, e);
    }
  }

  console.log(`✅ Repair complete!`);
  console.log(`📊 Updated: ${updateCount} products`);
  console.log(`📊 Not found: ${missingCount} (could be minor name mismatches or early manual additions)`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  });
